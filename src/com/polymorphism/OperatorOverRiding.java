package com.polymorphism;

public class OperatorOverRiding {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("(+) operator used for add two values : "+(a+b));
        String strOne="Hello";
        String strTwo="World";
        System.out.println("(+) operator used for String concatenation : "+(strOne+strTwo));
    }
}
